    <h3>Què és Taxo&Map?</h3>
    <p>Taxo&Map és una aplicació dissenyada per a la immersió en les dades de les col·leccions del Consorci del Museu de Ciències Naturals de Barcelona, MCNB. Taxo&Map ofereix unes opcions de consulta complementàries a les proporcionades per altres vies.</p>

    <p>La informació de les col·leccions de l'MCNB es pot consultar en línia des de diversos webs. La manera més directa de fer-ho és a través d’un servei tradicional de cerca mitjançant formulari. 
El Museu dóna suport a diversos portals que agreguen informació de biodiversitat de diverses fonts. El fons del Museu és consultable al portal GBIF, en l’actualitat el més universal, obert i gratuït.</p>

    <p>En els dos casos referits, la cerca d’informació parteix de les condicions prèvies plantejades per qui fa la consulta; després vénen els resultats. Taxo&Map segueix una estratègia inversa. En primer lloc es visualitza tota la informació i a partir d’aquí l’usuari practica moviments de zoom i desplaçaments en l’àmbit geogràfic i taxonòmic fins a seleccionar les dades que desitja observar o exportar.</p>
	
    <p>El projecte Taxo&Map és el resultat d’unir dues ambicions:</p>
	
    <ul>
        <li>La voluntat del Museu de mostrar les dades de les col·leccions en condicions de cerca més especulativa.</li>
        <li>La capacitat de disseny i d’anticipar necessitats i funcions que han demostrat els socis tecnològics del projecte.</li>
    </ul>
	
