    <h3>¿Qué es Taxo&Map?</h3>
    <p>Taxo&Map es una aplicación diseñada para la inmersión en los datos de las colecciones del Consorcio del Museo de Ciencias Naturales de Barcelona, MCNB. 
Taxo&Map ofrece unas opciones de consulta complementarias a las proporcionadas por otras vías.</p>

    <p>La información de las colecciones del MCNB se puede consultar en línea desde varias webs. La forma más directa es realizarlo a través de un servicio tradicional de búsqueda por formulario. 
El Museo da apoyo a varios portales que agregan información de biodiversidad de distintas fuentes. El fondo del Museo es consultable en el portal GBIF, actualmente el más universal, abierto y gratuito.</p>

    <p>En los dos casos referidos la búsqueda de información parte de las condiciones previas planteadas por quien formula la consulta; después vienen los resultados. Taxo&Map sigue una estrategia inversa. En primer lugar se visualiza toda la información y a partir de aquí el usuario practica movimientos de zoom y desplazamientos en el ámbito geográfico y taxonómico hasta seleccionar los datos que desea observar o exportar.</p>
	
    <p>Taxo&Map es el resultado de unir dos ambiciones:</p>
	
    <ul>
        <li>La voluntad del Museo de mostrar los datos de las colecciones en condiciones de búsqueda más especulativa.</li>
        <li>La capacidad de diseño y de anticipar necesidades y funciones que han demostrado los socios tecnológicos del proyecto.</li>
    </ul>
	
